-module(exe3).

%% API
-export([print/1, print_even/1]).

%%%===================================================================
%%% API
%%%===================================================================
print(X) -> each(1, X).

print_even(X) -> X.
%%--------------------------------------------------------------------
%% @doc
%% @spec
%% @end
%%--------------------------------------------------------------------

%%%===================================================================
%%% Internal functions
%%%===================================================================
each(X, X) -> display(X);
each(Y, X) ->
    display(Y),
    each(Y + 1, X).

display(Num) -> io:format("Number:~p~n", [Num]).
