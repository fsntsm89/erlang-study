-module(exe2).

%% API
-export([create/1, reverse_create/1]).

%%%===================================================================
%%% API
%%%===================================================================
create(X) -> create_list(X, []).

reverse_create(X) -> reverse(create_list(X, [])).
%%--------------------------------------------------------------------
%% @doc
%% @spec
%% @end
%%--------------------------------------------------------------------

%%%===================================================================
%%% Internal functions
%%%===================================================================
create_list(0, List) -> List;
create_list(X, List) -> create_list(X-1, [X|List]).

reverse(List) -> lists:reverse(List).
