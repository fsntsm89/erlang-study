compare(X, [], Small, Large) -> 
compare(X, [Y|Z], Small, Large) when X =< Y->
    compare(X, Z, Small, [Y|Large]);
compare(X, [Y|Z], Small, Large) ->
    compare(X, Z, [Y|Small], Large).
